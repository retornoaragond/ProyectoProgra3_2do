    boolean validar(){
        boolean error=false;
        
        this.codigoLabel.setForeground(SistemaActivos.COLOR_OK); 
        if (this.codigoLabel.getText().isEmpty()){
            this.codigoLabel.setForeground(SistemaActivos.COLOR_ERROR);
            error=true;
        }
        return !error;
    }
    
    
    
    
    
    
    
    
    
    
